<h1>Waste Segregation Guidelines</h1>
<p>Proper waste segregation helps keep the environment clean, reduces pollution, and promotes recycling. Follow these guidelines to properly separate and dispose of your waste.</p>

<h2>1. Biodegradable Waste (Green Bin)</h2>
<p>These are wastes that can decompose naturally and be turned into compost.</p>
<ul>
  <li>Fruit and vegetable peels</li>
  <li>Leftover food</li>
  <li>Paper, cardboard, and tissue</li>
  <li>Leaves, grass, and garden clippings</li>
</ul>

<h2>2. Non-Biodegradable Waste (Black Bin)</h2>
<p>These are wastes that do not decompose easily and can harm the environment if not properly disposed of.</p>
<ul>
  <li>Plastic bags and wrappers</li>
  <li>Styrofoam containers</li>
  <li>Broken glass and ceramics</li>
  <li>Metal scraps</li>
</ul>

<h2>3. Recyclable Waste (Blue Bin)</h2>
<p>These are materials that can be collected and processed to create new products.</p>
<ul>
  <li>Plastic bottles</li>
  <li>Aluminum cans</li>
  <li>Old newspapers</li>
  <li>Cardboard boxes</li>
</ul>

<h2>4. Residual Waste (Gray Bin)</h2>
<p>These are wastes that cannot be reused, recycled, or composted.</p>
<ul>
  <li>Used diapers and sanitary napkins</li>
  <li>Used tissue papers</li>
  <li>Cigarette butts</li>
  <li>Dirty or contaminated plastics</li>
</ul>

<h2>5. Hazardous Waste (Red Bin)</h2>
<p>These are harmful wastes that must be handled carefully to avoid danger to people and the environment.</p>
<ul>
  <li>Batteries</li>
  <li>Old medicines and medical waste</li>
  <li>Chemical containers</li>
  <li>Paints, thinners, and cleaning agents</li>
</ul>

<h2>Segregation Tips</h2>
<ul>
  <li>Label your bins clearly with colors or signs.</li>
  <li>Keep recyclable items clean and dry before disposal.</li>
  <li>Do not mix hazardous waste with other types of waste.</li>
  <li>Compost biodegradable waste if possible.</li>
  <li>Encourage family and community members to practice segregation daily.</li>
</ul>

<p><strong>Remember:</strong> Proper waste segregation starts at home. Small actions lead to a cleaner, healthier environment for everyone.</p>

